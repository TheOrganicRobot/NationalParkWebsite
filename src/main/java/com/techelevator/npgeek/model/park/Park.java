package com.techelevator.npgeek.model.park;

public class Park {
	
	private String parkCode;
	private String name;
	private String state;
	private int acreage;
	private int elevationInFt;
	private double milesOfTrail;
	private int numOfCampsites;
	private String climate;
	private int yearFounded;
	private int annualVisitors;
	private String quote;
	private String quoteAuthor;
	private String description;
	private int entryFee;
	private int speciesCount;
	
	
	public String getParkCode() {
		return parkCode;
	}
	public void setParkCode(String parkCode) {
		this.parkCode = parkCode;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public int getAcreage() {
		return acreage;
	}
	public void setAcreage(int acreage) {
		this.acreage = acreage;
	}
	public int getElevationInFt() {
		return elevationInFt;
	}
	public void setElevationInFt(int evelvationInFt) {
		this.elevationInFt = evelvationInFt;
	}
	public double getMilesOfTrail() {
		return milesOfTrail;
	}
	public void setMilesOfTrail(double milesOfTrail) {
		this.milesOfTrail = milesOfTrail;
	}
	public int getNumOfCampsites() {
		return numOfCampsites;
	}
	public void setNumOfCampsites(int numOfCampsites) {
		this.numOfCampsites = numOfCampsites;
	}
	public String getClimate() {
		return climate;
	}
	public void setClimate(String climate) {
		this.climate = climate;
	}
	public int getYearFounded() {
		return yearFounded;
	}
	public void setYearFounded(int yearFounded) {
		this.yearFounded = yearFounded;
	}
	public int getAnnualVisitors() {
		return annualVisitors;
	}
	public void setAnnualVisitors(int annualVisitors) {
		this.annualVisitors = annualVisitors;
	}
	public String getQuote() {
		return quote;
	}
	public void setQuote(String quote) {
		this.quote = quote;
	}
	public String getQuoteAuthor() {
		return quoteAuthor;
	}
	public void setQuoteAuthor(String quoteAuthor) {
		this.quoteAuthor = quoteAuthor;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public int getEntryFee() {
		return entryFee;
	}
	public void setEntryFee(int entryFee) {
		this.entryFee = entryFee;
	}
	public int getSpeciesCount() {
		return speciesCount;
	}
	public void setSpeciesCount(int speciesCount) {
		this.speciesCount = speciesCount;
	}
	

}
